package ru.arsmagna;

/**
 * Common separator symbols.
 */
@SuppressWarnings("unused")
public class CommonSeparators {

    /**
     * Comma
     */
    public static final char[] comma = { ',' };

    /**
     * Comma or semicolon.
     */
    public static final char[] commaOrSemicolon = { ',', ';'};

    /**
     * Colon.
     */
    public static final char[] colon = { ':'};

    /**
     * Dot.
     */
    public static final char[] dot = { '.'};

    /**
     * Minus sign.
     */
    public static final char[] minus = {'-'};

    /**
     * New line.
     */
    public static final char[] newLine = {'\r', '\n'};

    /**
     * Newline or percent sign.
     */
    public static final char[] newLineOrPercent = { '\r', '\n', '%'};

    /**
     * Number sign.
     */
    public static final char[] numberSign = { '#'};

    /**
     * Semicolon.
     */
    public static final char[] semicolon = {';'};

    /**
     * Slash.
     */
    public static final char[] slash = {'/'};

    /**
     * Space.
     */
    public static final char[] space = { ' '};

    /**
     * Space or tabulation sign.
     */
    public static final char[] spaceOrTab = {' ', '\t'};

    /**
     * Tabulation sign.
     */
    public static final char[] tab = {'\t'};

    /**
     * Vertical line.
     */
    public static final char[] verticalLine = {'|'};

}
